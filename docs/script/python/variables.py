#	Variables	#
#################

myInt = 100
string = "foo"
myFloat = 1.25
myBool = True

#   printing variable types
print(type(myInt))
print(type(string))
print(type(myFloat))
print(type(myBool))

#   printing a type conversion
print(type(float(myInt)))
print(type(str(myFloat)))
print(type(int(myBool)))
